#pragma once
#include <Windows.h>

enum JumpType
{
  UNKNOWN,
  JO,   // 0x70 / 0x0F 80
  JNO,  // 0x71 / 0x0F 81
  JB,   // 0x72 / 0x0F 82
  JNB,  // 0x73 / 0x0F 83
  JE,   // 0x74 / 0x0F 84
  JNE,  // 0x75 / 0x0F 85
  JBE,  // 0x76 / 0x0F 86
  JA,   // 0x77 / 0x0F 87
  JS,   // 0x78 / 0x0F 88
  JNS,  // 0x79 / 0x0F 89
  JP,   // 0x7A / 0x0F 8A
  JNP,  // 0x7B / 0x0F 8B
  JL,   // 0x7C / 0x0F 8C
  JGE,  // 0x7D / 0x0F 8D
  JLE,  // 0x7E / 0x0F 8E
  JG,   // 0x7F / 0x0F 8F
  JCXZ, // 0xE3
  JMP   // 0xE9 / 0xEB
};

struct Nanomite
{
  DWORD Rva;  // Relative to ImageBase
  DWORD JumpType;
  DWORD JumpLength;
  DWORD OpcodeLength;
};